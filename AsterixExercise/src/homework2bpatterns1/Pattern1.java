package homework2bpatterns1;

public class Pattern1 {
	 public static void main(String args [])  
	    { 
	 
			 
	        int i, j, n = 5; 
	  	              
	        for(i=1; i<n; ++i) 
	        { 
	                 
	            for(j=0; j<=i; ++j) 
	            { 
	                System.out.print("* "); 
	            } 
	            	
	            	System.out.println(); 
	            	
	        } 
	    }
}
	  
	    
	 