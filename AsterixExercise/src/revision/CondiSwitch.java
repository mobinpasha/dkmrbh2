package revision;

public class CondiSwitch {
static int weight =40;

public static void main(String [] args) {
	
switch (weight){

case 30:
	System.out.println("underweight");
break;	
case 35:
	System.out.println("average");
break;	
case 40:
	System.out.println("standard");
break;	
case 50:
	System.out.println("over");
break;	

	}
}
}